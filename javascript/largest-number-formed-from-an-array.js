/*
Given a list of non negative integers, arrange them in such a manner 
that they form the largest number possible.The result is going to be very large, 
hence return the result in the form of a string.


Example 1:

Input: 
N = 5
Arr[] = {3, 30, 34, 5, 9}
Output: 9534330
Explanation: Given numbers are {3, 30, 34,
5, 9}, the arrangement 9534330 gives the
largest value.

Example 2:

Input: 
N = 4
Arr[] = {54, 546, 548, 60}
Output: 6054854654
Explanation: Given numbers are {54, 546,
548, 60}, the arrangement 6054854654 
gives the largest value.


Your Task:  
You don't need to read input or print anything. Your task is to complete the function printLargest() 
which takes the array of strings arr[] as parameter and returns a string denoting the answer.

Expected Time Complexity: O(NlogN)
Expected Auxiliary Space: O(1)
*/

class Solution {
    printLargest(arr) {
        let t = [];
        for (let i = 0; i < arr.length; i++) {
            t.push(arr[i].toString());
        }

        t.sort((a, b) => (b + a) - (a + b));
        return t.join('');
    }
}